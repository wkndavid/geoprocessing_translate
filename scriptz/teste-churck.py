# Função para traduzir uma palavra de acordo com o dicionário de traduções
def traduzir_palavra(palavra, traducoes):
    return traducoes.get(palavra, palavra)  # Retorna a tradução se existir, caso contrário, retorna a palavra original

# Função para traduzir as colunas do DataFrame
def traduzir_colunas(df, colunas, traducoes):
    for coluna in colunas:
        df[coluna] = df[coluna].apply(lambda x: ' '.join(traduzir_palavra(palavra, traducoes)
                                                         for palavra in re.findall(r'\b\w+\b', str(x))).upper())
    return df

